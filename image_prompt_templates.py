def is_clothing_image_category(category_profile: dict[str, Any] | None) -> bool:
    category = str((category_profile or {}).get("category") or "").casefold()
    return any(marker in category for marker in CLOTHING_CATEGORY_MARKERS)


def build_image_template_prompts(
    *,
    product_description: str,
    images_count: int,
    category_profile: dict[str, Any] | None,
    image_guidance: str = "",
) -> list[tuple[str, str]]:
    category = str((category_profile or {}).get("category") or "").strip()
    slides = CLOTHING_SLIDES if is_clothing_image_category(category_profile) else UNIVERSAL_SLIDES
    product_text = product_description.strip()
    guidance_text = image_guidance.strip()
    prompts: list[tuple[str, str]] = []

    for purpose, slide_brief in slides[:images_count]:
        prompt_parts = [
            slide_brief,
            "",
            f"Категория товара: {category}" if category else "",
            f"Данные товара:\n{product_text}",
            f"Пожелания к изображениям:\n{guidance_text}" if guidance_text else "",
            "",
            "Визуальная подача: фотореализм, коммерческая ретушь, современная карточка для Wildberries/Ozon, формат 3:4.",
            COMMON_FACT_GUARD,
        ]
        prompts.append((purpose, "\n".join(part for part in prompt_parts if part).strip()))
    return prompts
